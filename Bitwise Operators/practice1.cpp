#include<iostream>
using namespace std;

int main(){

    int a = 4;
    int b = 6;
    int i = 10;

    cout<<" a&b "<< (a&b )<<endl;

    cout<<" a|b "<<(a|b) <<endl;

    cout<<" ~a "<< ~a <<endl;

    cout<<" a^b "<< (a^b) <<endl;

    cout<<" 15 >> 2 "<< (15>>2) <<endl;

    cout<<" 15 << 2 "<< (15<<2) <<endl;

    cout<< " i++ " << (i++) <<endl;

    cout<< " i-- " << (i--) <<endl;

    cout<< " ++i " << (++i) <<endl;

    cout<< " --i " << (--i) <<endl;






}